package com;

public class Custom2 {
    int images;
    String name,price;

    public Custom2(int images, String name, String price) {
        this.images = images;
        this.name = name;
        this.price = price;
    }

    public int getImages() {
        return images;
    }

    public String getName() {
        return name;
    }

    public String getPrice() {
        return price;
    }
}
